// Export a single chart div as PNG using Plotly's built-in toImage
export async function exportChartAsPNG(plotlyDiv, filename = 'chart') {
  const Plotly = await import('plotly.js/dist/plotly.min.js')
  const dataUrl = await Plotly.default.toImage(plotlyDiv, {
    format: 'png',
    width: 1200,
    height: 600,
    scale: 2,
  })
  const link = document.createElement('a')
  link.href = dataUrl
  link.download = `${filename}.png`
  link.click()
}


